#!/bin/bash
###############################################################################
# Generate Datasets
#   
###############################################################################
USRM=$1
PTHO=$2

###############################################################################
# Terminal Colors
###############################################################################
RED='\033[0;31m'
NC='\033[0m' # No Color
###############################################################################
# Run Scripts
###############################################################################
printf "${RED}* Cleaning Last.fm's dataset...${NC}\n"
python Clean_Lastfm.py "$USRM" "$PTHO/dta"
printf "${RED}* Downloading Musicbrainz artists' data...${NC}\n"
python Download_Musicbrainz.py "$USRM" "$PTHO/dta"